                <h2 class="header-text-right">Allergies</h2><br>
                <h5>What are allergies?</h5><br>
                <p>Allergies are your body’s reaction to a substance it views as a harmful “invader.” For example, coming into contact with what is normally
                    a harmless substance, such as pollen, might cause your immune system (your body’s defense system) to react. Substances that cause these reactions
                    are called allergens.</p><br>
                <h5>What is an allergic reaction?</h5><br>
                <p>An “allergic reaction” is the way your body responds to the allergen. A chain of events occurs that results in an allergic reaction.
                    If you are prone to allergies, the first time you’re exposed to a specific allergen (such as pollen), your body responds by producing allergic (IgE) antibodies.
                    The job of these antibodies is to find the allergens  and help remove them from your system. As a result, a chemical called histamine is released  and
                    causes symptoms of allergies.</p><br>
                <h5>What are the types of allergies  and how are they treated?</h5><br>
                <p>You can be allergic to a wide variety of substances – including pollen, animal dander, mold, and dust mites.</p><br>
                <h6>Pollen</h6><br>
                <p>Seasonal allergic rhinitis, or hay fever, is an allergic response to pollen. It causes inflammation  and swelling of the lining of your nose  and the protective tissue of your eyes (conjunctiva).</p><br>
                <p>Symptoms include sneezing, congestion (feeling stuffy), and itchy, watery eyes, nose, and mouth. Treatment options include over-the-counter and prescription oral antihistamines, anti-leukotrienes, nasal steroids, nasal antihistamines, and nasal cromolyn. In some people, allergic asthma symptoms (wheezing, shortness of breath, coughing, and/or chest tightness) can be caused by exposure to pollen.</p><br>
                <p>Your symptoms can be reduced by avoiding pollen. Stay indoors when pollen counts are high, close your windows,
                     and use air conditioning. Ask your healthcare provider about immunotherapy (“allergy shots”) to treat pollen allergy.</p><br>
                <h6>Dust mites</h6><br>
                <p>Dust mites are tiny organisms that live in dust and the fibers of household objects, such as pillows, mattresses, carpets, and upholstery. Dust mites grow in warm, humid areas.</p><br>
                <p>The symptoms of dust mite allergy are similar to those of pollen allergy. To help manage dust mite allergies, try using dust mite encasements
                    (airtight plastic/polyurethane covers) over pillows, mattresses,  and box springs. Also, remove carpet, or vacuum frequently with a high-efficiency
                    filter vacuum cleaner. Treatment may include medications to control your nasal/eye  and chest symptoms. Immunotherapy may be recommended if your
                    symptoms are not adequately controlled with avoidance methods  and medications.</p><br>
                <h6>Molds</h6><br>
                <p>Molds are tiny fungi (like Penicillium) with spores that float in the air like pollen. Mold is a common trigger for allergies.
                    Mold can be found indoors in damp areas, such as the basement, kitchen, or bathroom, as well as outdoors in grass, leaf piles, hay,
                    mulch, or under mushrooms. Mold spores reach a peak during hot, humid weather.
                    Treatment may include medications to control your nasal/eye  and chest symptoms. Immunotherapy may be recommended if your symptoms are not
                    adequately controlled with avoidance  and medications.</p><br>
                <h6>Animal dander</h6><br>
                <p> Allergic reactions can be caused by the proteins secreted by sweat glands in an animal’s skin, which are shed in dander, and by the proteins in an animal’s saliva. Avoidance measures don’t work as well as simply removing the pet from your home. However, because many people are reluctant to do this, second-best measures include keeping your pet out of your bedroom, using air cleaners with HEPA filtration, and washing your pet (cat or dog) frequently.</p><br>
                <h6>Latex</h6><br>
                <p>Some people develop a latex allergy after repeated contact with latex. Rubber gloves, such as those used in surgery or home cleaning, are a major source for causing this
                    type of reaction. Skin rash, hives, eye tearing,  and irritation, wheezing and itching of the skin may occur if you have a latex allergy.</p><br>
                <p>Allergic reactions to latex can be mild, such as skin redness  and itching. More severe reactions can occur if your mucosal membranes are exposed, such as during an
