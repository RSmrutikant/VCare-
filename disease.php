                <p>Tension headaches are the most common type of headache. Tension headache pain tends to be:</p>


                <ul>
                    <li>Consistent without throbbing.</li>
                    <li>Mild to moderate.</li>
                    <li>On both sides of the head (bilateral).</li>
                    <li>Responsive to over-the-counter treatment.</li>
                    <li>Worse during routine activities (such as bending over or walking upstairs).</li>
