                <p>Allergy symptoms are classified as mild, moderate, or severe:</p><br>
                <ul>
                    <li>Mild reactions include local symptoms (affecting a specific area of your body) such as a rash or hives, itchiness, watery/red eyes, hay fever,  and runny nose.
                        Mild reactions do not spread to other parts of your body.</li>
                    <li>Moderate reactions include symptoms that spread to other parts of your body. Symptoms may include itchiness, hives  and/or swelling,  and trouble breathing.</li>
                    <li>A severe allergic reaction, known as anaphylaxis, is a rare, life-threatening emergency in which your body’s response to the allergen is sudden  and affects the whole body.
                        Anaphylaxis may begin with severe itching of your eyes or face. Within minutes, more serious symptoms appear, including throat swelling (which could cause problems with
                        swallowing  and breathing), abdominal pain, cramps, vomiting, diarrhea, hives,  and swelling (angioedema). You may also have mental confusion or dizziness, since anaphylaxis
                        may cause a drop in blood pressure.</li>
                </ul><br>
                <h5>What causes allergies?</h5><br>
                <p>Anything that you come into contact with that your body views as a “harmful invader” can be a cause of an allergy. Normally harmless substances that are
                    common causes of allergies include pollen, animal dander, mold, dust, foods, insect venom,  and latex</p><br>
                <p>Technically, your symptoms are the result of a chain of events that are your body’s response to the “harmful invader.” Your body “sees” the invader,
                    makes antibodies to fight the invader  and in so doing, releases histamines that cause your allergy symptoms.</p><br>
                <h3>DIAGNOSIS  AND TESTS</h3><br>
                <h5>How are allergies diagnosed?</h5><br>
                <p>If you think you have allergies, don't wait to see if your symptoms go away.</p><br>
                <p>When your symptoms last longer than a week or two  and tend to come back, make an appointment with an allergy/immunology specialist.</p><br>
                <p>Allergy skin testing may be used to identify the allergens that are causing your allergy symptoms. The test is performed by pricking your skin with an extract of an allergen and then checking your skin’s reaction. If a skin test can’t be performed, blood work may be obtained. This test is not as sensitive as a skin test. The test evaluates the number of antibodies produced by your immune system. Higher levels of certain antibodies suggest a possible allergy to that allergen. Other types of allergy testing are available too.</p><br>
                <h3>MANAGEMENT  AND TREATMENT</h3><br>
                <p>How are allergies treated?</p><br>
                <p>Although avoiding the allergen is an important treatment approach, it usually doesn’t completely end the allergic reaction.</p><br>
                <p>Medications such as antihistamines (e.g., Allegra®, Zyrtec®), decongestants (eg, Sudafed®, Contact®), or a combination of over-the-counter  and prescription medications, are used to treat your allergy symptoms. Nasal sprays such as topical nasal steroids (e.g., Flonase®, Nasonex®), cromolyn sodium,  and topical nasal antihistamines also can be used to treat allergy symptoms.
                    Asthma medications, which reduce allergy symptoms, include:</p><br>
                <ul>
                    <li>Inhaled bronchodilators.</li>
                    <li>Inhaled steroids.</li>
                    <li>Oral bronchodilators (theophylline).</li>
                    <li>Oral anti-leukotrienes (montelukast [Singulair®], zafirlukast [Accolate®]  and zileuton [Zyflo®]).</li>
                    <li>Injected medications, such as omalizumab (Xolair®), dupilumab (Dupixent®), reslizumab (Cinqair®), benralizumab (Fasenra®), or Mepolizumab (Nucala®).</li>
                </ul><br>
                <p>Immunotherapy (“allergy shot therapy”) or allergy oral immunotherapy is recommended if your symptoms aren’t adequately controlled with a combination of
                    avoidance measures  and regular medication use. This shot has been shown to be effective in properly selected patients with allergic rhinitis  and/or allergic asthma.</p><br>
                <p>Another treatment option is saline irrigation using a sinus rinse kit. These rinse kits (e.g., Neilmed®) are sold over-the-counter or can be made at home.
                    To make your rinse, combine one-half teaspoon non-iodinated salt with one-half teaspoon baking soda in eight ounces of distilled or boiled water.
                    This mixture rinses out allergens  and decreases the amount of inflammation (edema) they cause.</p><br>
                <h3>OUTLOOK / PROGNOSIS</h3><br>
                <p>Can allergies be cured?</p><br>
                <p>Allergies can’t be cured, but symptoms can be controlled using a combination of avoidance measures  and medications, as well as allergen immunotherapy in properly selected cases</p>

