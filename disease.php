                <h6>Oseltamivir phosphate</h6><br>
                <p>This drug is approved to treat influenza in patients who are two weeks of age,  and older,
                     and it works best in people who have had the flu for fewer than two days. It is also approved to prevent
                    flu in patients who are one year of age  and older. There is a generic version of this product available, but it
