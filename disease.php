                <h2>Gout</h2><br>
                <p>A gout is a painful form of arthritis. When your body has extra uric acid, sharp crystals may form in the big toe or other joints, causing episodes of swelling  and pain called gout attacks. Gout is treatable with medications  and changes in diet  and lifestyle.</p>
                <br>
                <h3>OVERVIEW</h3><br>
                <h5>What is gout?</h5><br>
                <p>Doctors place gout under the umbrella term “arthritis” — a broad range of joint diseases  and joint pain. Some forms of arthritis inflame joints, while others don’t.A Gout is a common form of inflammatory arthritis. It’s due to a crystal called uric acid.<br><br>
                   A gout causes pain  and swelling in one or more joints. It typically affects the big toe. But it’s also found in other joints, including the knee, ankle, foot, hand, wrist,  and elbow.
                </p>
                <h3>Who is affected by gout?</h3><br>
                <p>A gout can affect anyone. It usually occurs earlier in men than women. It generally occurs after menopause in women. Men can be three times more likely than women to get it because they have higher levels of uric acid most of their lives. Women reach these uric acid levels after menopause.</p>
                <br>
                <p>People are more likely to get gout if they have:</p><br>
                <ul>
                    <li> Obesity, or a lot of extra weight.</li>
                    <li>Congestive heart failure.</li>
                    <li>Diabetes.</li>
                    <li>Family history of gout.</li>
                    <li>Hypertension (high blood pressure)./li>
                    <li>Kidney disease.</li>
                </ul><br>
                <p>You are also more likely to develop gout if you:</p><br>
                <ul>
                    <li>Consume a diet high in animal proteins</li>
                    <li>Consume a significant amount of alcohol</li>
                    <li>Are on water pills (diuretics).</li>
                </ul><br>
                <h3>SYMPTOMS  AND CAUSES</h3>
                <h5>What causes gout?</h5><br>
                <p>The human body makes uric acid during the breakdown of chemicals called purines found in certain food  and drinks. This normal byproduct goes through the kidneys  and exits the body when you pee.<br>
                    Sometimes the body produces too much uric acid. Or the kidneys can’t do a good job handling it. When the body has high levels of uric acid or hyperuricemia, uric acid crystals can concentrate in the joints. The sharp, needle-like crystals cause gout. However, many people with higher uric acid levels never get gout.
                </p><br>
                <h5>What are the symptoms of gout?</h5><br>
                <p>An episode of gout is called a gout attack. Gout attacks are very painful  and can happen quite suddenly, often overnight. During a gout attack, symptoms in the affected joint(s) may include:</p><br>
                <ul>
                    <li>Intense pain.</li>
                    <li>Redness.</li>
                    <li>Stiffness.</li>
                    <li>Swelling.</li>
                    <li>Tenderness, even to light touch, such as from a bedsheet.</li>
                    <li>Warmth or a feeling like the joint is “on fire.”</li>
                    <li>How long does a gout attack last?</li>
                </ul><br>
                <p>A gout attack can last a week or two. Between gout attacks, you may have no symptoms at all.</p><br>
                <h5>How often do gout attacks happen?</h5><br>
                <p>Some people have gout attacks frequently, while others go years between episodes. If gout isn’t treated, attacks may become more frequent  and last longer. Gout attacks can happen over  and over again in the same joint or affect different joints.</p><br>
                <h3>DIAGNOSIS  AND TESTS</h3><br>
                <h5>How does a doctor diagnose gout?</h5><br>
                <p>If you have sudden or severe pain in a joint, you should talk to your primary care provider (PCP). Your PCP may send you to a rheumatologist, a doctor who specializes in gout  and other kinds of arthritis.</p><br>
                <p>Healthcare providers consider several things when confirming gout:</p><br>
                <ul>
                    <li><b>Symptoms: </b> The provider will ask you to describe your symptoms, how often they happen  and how long they last.</li>
                    <li><b>Physical examination: </b> Your provider will examine the affected joint(s) to look for swelling, redness,  and warmth.</li>
                    <li><b>Blood work: </b>A test can measure the amount of uric acid in your blood.</li>
                    <li><b>Imaging tests: </b>You may have pictures taken of the affected joint(s) with X-rays, an ultrasound, or MRI.</li>
                    <li><b>Aspiration:</b> The provider may use a needle to pull fluid from the joint. Using a microscope, a team member can look for uric acid crystals (confirming gout) or a different problem (such as bacteria with infection or another type of crystal).</li>
                </ul><br>
                <h3>MANAGEMENT  AND TREATMENT</h3><br>
                <h5>How is gout treated?</h5><br>
                <p>Your healthcare provider may prescribe certain medications to treat gout.</p><br>
                <p>Some drugs help control symptoms:</p><br>
                <ul>
                    <li>NSAIDs can reduce pain  and swelling. Some people with kidney disease, stomach ulcers,  and other health problems are unable to take NSAIDs.</li>
                    <li>Colchicine can reduce inflammation  and pain if you take it within 24 hours of a gout attack. It’s given by mouth.</li>
                    <li>Corticosteroids can relieve pain  and swelling. You take steroids by mouth or with an injection.</li>
                </ul><br>
                <p>Drugs that help lower levels of uric acid in your body to prevent or reduce future episodes of gout attacks:</p><br>
                <ul>
                    <li>Allopurinol, taken as a pill.</li>
                    <li>Febuxostat, taken as a pill.</li>
                    <li>Pegloticase, given as an intravenous (in the vein) infusion.</li>
                    <li>Probenecid, taken as a pill.</li>
                </ul><br>
                <h3>PREVENTION</h3><br>
                <h5>Can I prevent gout?</h5><br>
                <p>You can make certain lifestyle changes to help prevent gout:</p><br>
                <ul>
                    <li>Drink plenty of water to help your kidneys function better  and avoid dehydration.</li>
                    <li>Exercise regularly to stay at a healthy weight. Extra weight increases uric acid in your body  and puts more stress on joints.</li>
                </ul><br>
                <p>Do your best to limit the purines in your body, since these chemicals can trigger uric acid buildup. Foods  and drinks containing high purine levels include:</p><br>
                <ul>
                    <li>Alcohol.</li>
                    <li>Red meat  and organ meats (liver, for example).</li>
                    <li>Shellfish.</li>
                    <li>Gravy.</li>
                    <li>Drinks  and foods high in fructose (fruit sugar).</li>
                    <li>Protein from animal sources. All protein from animal flesh can potentially lead to elevated uric acid levels.</li>
                </ul><br>
                <p>Certain medications can lead to elevated uric acid levels. These medications include:</p><br>
                <ul>
                    <li>Diuretics, also known as “water pills.”</li>
                    <li>Immunosuppressants or drugs are used to slow the immune system (common in organ transplants, for example).</li>
                </ul><br>
                <h5>OUTLOOK / PROGNOSIS</h5><br>
                <h3>When should I call my healthcare provider about gout symptoms?</h3><br>
                <p>If you experience sudden, intense pain in a joint, call a healthcare provider right away. If the joint is hot  and inflamed, you might have gout — or you might have another problem like an infection.</p><br>
                <h5>What’s the outlook for people with gout?</h5><br>
                <p>Untreated gout can lead to permanent joint damage. The buildup of uric acid in the joints  and soft tissue is called tophus. Some people with gout can also develop other health problems, such as severe arthritis, kidney stones,  and heart disease. It’s important to discuss your symptoms with a healthcare provider.</p><br>
                <h3>LIVING WITH</h3><br>
                <h5>How can I manage a gout attack?</h5><br>
                <p>When you have a gout attack, you can manage your symptoms by:</p><br>
                <ul>
                    <li>Avoid alcohol  and sweet drinks.</li>
                    <li>Drinking plenty of fluids.</li>
                    <li>Elevating the joint(s).</li>
                    <li>Putting ice on the joint(s).</li>
                    <li>Limiting any stress on the joint(s).</li>
                </ul><br>
                <h5>What else should I ask my healthcare provider about gout?</h5><br>
                <p>Consider asking your healthcare provider:</p><br>
                <ul>
                    <li>What is causing gout?</li>
                    <li>Do I have any joint damage?</li>
                    <li>What can I do to prevent future attacks?</li>
                    <li>Can any gout medications help me?</li>
                    <li>How long will I need to take gout medications?</li>
                </ul><br>
                <p>Gout is a painful form of arthritis. Extra uric acid in your body creates sharp crystals in the joints, leading to swelling  and extreme tenderness. Gout usually starts in the big toe but can affect other joints. Gout is a treatable condition  and the uric acid level can be decreased by medication  and lifestyle changes. Talk to your healthcare provider about medications that can reduce uric acid levels. They can also discuss changes you can make to your diet  and lifestyle to prevent  and reduce gout attacks.</p><br>

