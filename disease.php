                <h3>SYMPTOMS  AND CAUSES</h3><br>
                <h5>Are headaches hereditary?</h5><br>
                <p>
                    Headaches tend to run in families, especially migraines. Children who have migraines usually have at least one parent
                    who also suffers from them. Kids whose parents have migraines are up to four times more likely to develop them too.</p>
                <p>Headaches can also be triggered by environmental factors shared in a family’s household, such as:</p>
                <ul>
                    <li>Eating certain foods or ingredients, like caffeine, alcohol, fermented foods, chocolate,  and cheese.</li>
                    <li>Exposure to allergens.</li>
                    <li>Second and smoke.</li>
                    <li>Strong odors from household chemicals or perfumes.</li>
                </ul><br>

                <h5>What causes headaches?</h5><br>
                <p>Headache pain results from signals interacting among the brain, blood vessels, and surrounding nerves. During a headache, an unknown
                    mechanism activates specific nerves that affect muscles  and blood vessels. These nerves send pain signals to the brain.</p><br>

                <h5>What causes migraines?</h5><br>
                <p>Migraines aren’t fully understood. But researchers think migraines result when unstable nerve cells overreact to various factors (triggers).
                    The nerve cells send out impulses to blood vessels  and cause chemical changes in the brain. The result is disabling pain.</p><br>
                <h5>What triggers headaches  and migraines?</h5><br>
                <p>Common triggers of tension headaches or migraines include:</p>

                <ul>
                    <li>Alcohol use.</li>
                    <li>Changes in eating or sleeping patterns.</li>
                    <li>Depression.</li>
                    <li>Emotional stress related to family  and friends, work, or school.</li>
                    <li>Excessive medication use.</li>
                    <li>Eye, neck, or back strain caused by poor posture.</li>
                    <li>Lighting.</li>
                    <li>Noise.</li>
                    <li>Weather changes</li>
                </ul><br><br>

